    public void AutoKodePre(){
           try{
                rs = conn.createStatement().executeQuery("SELECT * FROM penjualan ORDER BY id_pretransaksi DESC");
                if(rs.next()){
                    String kode = rs.getString("id_pretransaksi").substring(3);
                    String AN = "" + (Integer.parseInt(kode) + 1);
                    String Nol = "";
             
                    if(AN.length() == 1){
                        Nol = "000";
                    }else if(AN.length() == 2){
                        Nol = "00";
                    }else if(AN.length() == 3){
                        Nol = "0";
                    }else if(AN.length() == 4){
                        Nol = "";
                    }
                    txtIdPreTransaksi.setText("PS" + Nol + AN);
                } else{
                    txtIdPreTransaksi.setText("PS0001");
                }
                txtIdPreTransaksi.enable(false);
                rs.close();
           }catch (Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            } 
        }
    
