    private void txtJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyReleased
        if (txtIdPelanggan.getText().equals("") || txtIdBuku.getText().equals("") || txtHargaJual.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap Lengkapi Data");
        }
        else{
            if (txtJumlah.getText().toString().equals("")) {
                txtJumlah.setText("");
                txtTotal.setText("");
            }
            else{
                int stok = Integer.parseInt(String.valueOf(stok_tampung.getText()));
                int a    = Integer.parseInt(String.valueOf(txtJumlah.getText()));
                int b    = Integer.parseInt(String.valueOf(txtHargaJual.getText()));
                
                if (a > stok) {
                    JOptionPane.showMessageDialog(null, "Stok Hanya Tersedia " + stok);
                }
                else{
                    int jumlah = a * b;
                    txtTotal.setText(String.valueOf(jumlah));
                }
            }
        }
    }//GEN-LAST:event_txtJumlahKeyReleased

