    public void tabelPelanggan(){
        Object[] judul = {"Kode Pelanggan","Nama Pelanggan","Jenis Kelamin","Alamat"};
        tabmodel = new DefaultTableModel(null,judul){
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblPelanggan.setModel(tabmodel);
    }
    
    public void kodeOtomatis(){
           try{
                rs = conn.createStatement().executeQuery("SELECT * FROM pelanggan ORDER BY id_pelanggan DESC");
                if(rs.next()){
                     String kode = rs.getString("id_pelanggan").substring(3);
                     String AN = "" + (Integer.parseInt(kode) + 1);
                     String Nol = "";

                     if(AN.length() == 1){
                      Nol = "000";
                    }else if(AN.length() == 2){
                        Nol = "00";
                    }else if(AN.length() == 3){
                         Nol = "0";
                    }else if(AN.length() == 4){
                        Nol = "";
                    }
                    txtIdPelanggan.setText("PL" + Nol + AN);
                }
                else{
                    txtIdPelanggan.setText("PL0001");
                }
                txtIdPelanggan.enable(false);
                rs.close();
           }
           catch (Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    
    public void tampil(){
        try {
            statement = conn.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = statement.executeQuery("SELECT * FROM pelanggan");
            while (rs.next()) {
                Object[] data = {
                    rs.getString("id_pelanggan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("alamat"),
                };
                tabmodel.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
     public void reset(){
        txtNama.setText("");
        btnGrup.clearSelection();
        txtAlamat.setText("");
        
        btnHapus.setEnabled(false);
        btnUpdate.setEnabled(false);
    }
    
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if(radioL.isSelected() == false && radioP.isSelected() == false){
            JOptionPane.showMessageDialog(null,"Silahkan Lengkapi Data!");
        }
        else if(txtIdPelanggan.getText().toString().equals("") || txtNama.getText().toString().equals("") || txtAlamat.getText().toString().equals("")){
            JOptionPane.showMessageDialog(null,"Silahkan Lengkapi Data!");
        }
        else{
            conn = koneksi.Conn();
            try {
                String hasil;
                if (radioL.isSelected()) {
                    hasil = "L";
                }else{
                    hasil = "P";
                }
                statement = conn.createStatement();
                statement.executeUpdate("INSERT INTO pelanggan SET " + " id_pelanggan='"+ txtIdPelanggan.getText() +"', nama_pelanggan='"+ txtNama.getText() +"', jenis_kelamin='"+ hasil +"', alamat='"+ txtAlamat.getText() +"'");
                tabelPelanggan();
                tampil();
                JOptionPane.showMessageDialog(null, "Data Berhasil di Input");
                kodeOtomatis();
                btnGrup.clearSelection();
                txtNama.setText("");
                txtAlamat.setText("");
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

